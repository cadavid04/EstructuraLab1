    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        ColeccionesDAO cDAO = new FileColeccionesDAO();
        List<Colecciones> listaColecciones = cDAO.getAllColecciones();
        String reporteColecciones="REPORTE DE COLECCIONES" + '\n' + '\n' +"Desc_Colecciones   " + "Trimestre          " + "Exclusividad        " +'\n';
        int i=0;
        for (i=0;i < listaColecciones.size();i++){
            reporteColecciones +=listaColecciones.get(i).getDesc_coleccion() + listaColecciones.get(i).getAno_trimestre() + listaColecciones.get(i).getExclusividad() +'\n';
             } 
        JOptionPane.showMessageDialog(null, reporteColecciones);
    }//GEN-LAST:event_jButton1ActionPerformed

